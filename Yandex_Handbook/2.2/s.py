"""
Автоматизация безопасности

Группа исследователей собирается высадиться на остров невероятно
ровной формы, но разведка при помощи спутника выяснила,
что на острове есть зона зыбучих песков.

Для повышения безопасности экспедиции было решено разработать
систему оповещения, которая предупредит исследователей об опасности.
А для снижения расходов на производство было решено заказать
программное обеспечение.

Схема
https://new.contest.yandex.ru/41234/problem?id=149944/2022_10_12/ZHMJWMoxqb

Напишите программу, которая по координатам исследователя, будет сообщать
о безопасности в этой точке.

Формат ввода
Два рациональных числа — координаты исследователя.

Формат вывода
Одно из сообщений:
- Опасность! Покиньте зону как можно скорее!
- Зона безопасна. Продолжайте работу.
- Вы вышли в море и рискуете быть съеденным акулой!
"""

x = float(input())
y = float(input())

if (x * x + y * y) > 100:
    print("Вы вышли в море и рискуете быть съеденным акулой!")
elif ((y <= 0 and (y >= (x + 1) ** 2 / 4 - 9))
      or (x >= 0 and y >= 0 and ((x * x + y * y) <= 25))
      or (-4 <= x <= 0 and 0 <= y <= 5)
      or ((-7 <= x <= -4 and 0 <= y <= 5) and 5 * x - 3 * y - 35 >= 0)):
    print("Опасность! Покиньте зону как можно скорее!")
else:
    print("Зона безопасна. Продолжайте работу.")
